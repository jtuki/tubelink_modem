/***************************************************************************************************
 * @fn      chksum_Bcc()
 *
 * @brief   check sum for bcc
 *
 * @author  author name
 *
 * @param   none
 *
 * @return  none
 */
unsigned char chksum_Bcc( unsigned char *a_pu8Data, unsigned long a_u16Length )
{
    unsigned char u8Ret = 0x00;

    if(0 != a_pu8Data)
    {
        while(a_u16Length > 0)
        {
            u8Ret ^= *a_pu8Data;
            a_pu8Data ++;
            a_u16Length --;
        }
    }

    return u8Ret;

} /* Hal_SpiInit() */



